import math

def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

def log(a):
    return math.log(a)

def square(a):
    return a * a

def sin(a):
    return math.sin(a)

def cos(a):
    return math.cos(a)

def sqrt(a):
    return math.sqrt(a)

def percentage(a, total):
    return (a / 100) * total